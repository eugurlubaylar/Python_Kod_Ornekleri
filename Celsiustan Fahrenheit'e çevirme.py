# Python Program to convert temperature in celcius to fahrenheit
# Input is provided by the user in degree celcius

# take input from the user
celcius = float(input("Enter degree Celciuss: "))

# calculate fahrenheit
fahrenheit = (celcius * 1.8) + 32
print("%0.1f degree Celcius is equal to %0.1f degree Fahrenheit" %(celcius, fahrenheit))

